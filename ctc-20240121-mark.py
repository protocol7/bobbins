from solver import Solver, _

# https://www.youtube.com/watch?v=vKVJhPZRUOo

solver = (
    Solver()
    .thermos([
        [(4, 0), (3, 1), (2, 2), (1, 3), (0, 4)],
        [(8, 3), (7, 3), (6, 2), (5, 1), (5, 0)],
        [(0, 5), (1, 5), (2, 6), (3, 7), (3, 8)],
        [(4, 8), (5, 7), (6, 6), (7, 5), (8, 4)],
    ])
    .xs([
        [(0, 0), (0, 1)],
        [(7, 5), (8, 5)],
        [(7, 7), (7, 8)],
        [(0, 8), (1, 8)],
        [(2, 8), (3, 8)],
    ])
    .whisper_lines([
        [(3, 2), (4, 3), (5, 4), (6, 5), (5, 6), (4, 5), (3, 4), (2, 3)]
    ])
    .region_sum_lines([
        [[(5, 2)], [(6, 3)], [(5, 4), (4, 5)], [(3, 6)], [(2, 5)], [(3, 4), (4, 3)]]
    ], True)
)

solution = solver.solve()

Solver.pretty_print(solution)
