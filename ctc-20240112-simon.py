from solver import Solver

# https://www.youtube.com/watch?v=aI03nXLTyGg

solver = (
    Solver()
    .between_lines([
        [(1, 0), (0, 0), (0, 1)],
        [(3, 0), (2, 1), (1, 2), (0, 3)],
        [(4, 1), (5, 1), (5, 2)],
        [(8, 1), (7, 2), (8, 3)],
        [(1, 4), (1, 5), (2, 5)],
        [(8, 5), (7, 6), (6, 7), (5, 8)],
        [(1, 8), (2, 7), (3, 8)],
        [(7, 8), (7, 7), (8, 7)],
        [(7, 8), (8, 8), (8, 7)],
    ])
    .circles([
        (1, 0), (2, 0), (3, 0), (5, 0), (6, 0), (7, 0),
        (0, 1), (4, 1), (8, 1),
        (0, 2), (4, 2), (5, 2), (8, 2),
        (0, 3), (4, 3), (6, 3), (8, 3),
        (1, 4), (7, 4),
        (0, 5), (2, 5), (4, 5), (8, 5),
        (0, 6), (3, 6), (4, 6), (8, 6),
        (0, 7), (4, 7), (8, 7),
        (1, 8), (2, 8), (3, 8), (5, 8), (6, 8), (7, 8),
    ])
)

solution = solver.solve()

Solver.pretty_print(solution)
