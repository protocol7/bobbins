from solver import Solver, _

# https://www.youtube.com/watch?v=R_2H-U_ljio

given = (
    (_, _, _, 6, _, _, _, _, _),
    (_, _, _, _, _, _, _, _, _),
    (_, _, _, _, _, _, _, _, _),
    (_, _, _, _, _, _, _, _, _),
    (_, _, _, _, _, _, 5, _, _),
    (_, _, _, _, _, _, _, _, _),
    (_, _, _, _, _, _, _, _, _),
    (_, _, _, _, _, _, _, _, _),
    (_, _, _, _, _, _, _, _, _),
)


solver = (
    Solver(given)
    .killer_cage([(0, 0), (1, 0)], 6)
    .killer_cage([(4, 0), (4, 1), (3, 1)], 12)
    .killer_cage([(7, 0), (8, 0)], 4)
    .killer_cage([(1, 4), (2, 4), (1, 5)], 9)
    .killer_cage([(7, 4), (8, 4), (7, 5)], 9)
    .killer_cage([(0, 6), (0, 7)], 5)
    .killer_cage([(8, 6), (8, 7)], 12)
    .killer_cage([(4, 7), (4, 8), (5, 8)], 7)

    .zipper_lines([
        [(0, 1), (1, 2), (2, 3), (3, 4), (2, 5), (1, 6), (0, 7)],
        [(2, 1), (3, 2), (4, 3), (5, 2), (6, 1)],
        [(8, 1), (7, 2), (6, 3), (5, 4), (6, 5), (7, 6), (8, 7)],
        [(1, 8), (2, 7), (3, 6), (4, 5), (5, 6), (6, 7), (7, 8)],
        [(3, 3), (4, 4), (5, 5)],
        [(5, 3), (4, 4), (3, 5)],
    ])
)


solution = solver.solve()

Solver.pretty_print(solution)
