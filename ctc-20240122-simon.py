from solver import Solver, _

# https://www.youtube.com/watch?v=dKZqSLK31HU

given = (
    (_, _, _, 6, 2, _, _, 3, _),
    (_, _, 2, _, _, _, _, _, _),
    (_, _, _, _, _, _, _, _, _),
    (_, _, _, _, _, _, _, _, _),
    (_, _, _, _, _, _, _, _, _),
    (_, _, _, _, _, 1, _, _, _),
    (_, 5, _, _, _, _, _, _, _),
    (_, _, _, _, _, _, _, _, _),
    (_, _, _, _, _, _, _, _, _),
)


solver = (
    Solver(given)
    .arrows([
        [(1, 1), (2, 2), (3, 3)],
        [(4, 1), (3, 2), (2, 3)],
        [(7, 1), (6, 2), (5, 3)],
        [(1, 4), (2, 5), (3, 6)],
        [(7, 4), (6, 3), (5, 2)],
        [(1, 7), (2, 6), (3, 5)],
        [(4, 7), (5, 6), (6, 5)],
        [(7, 7), (6, 6), (5, 5)],
    ])
)

solution = solver.solve()

Solver.pretty_print(solution)
